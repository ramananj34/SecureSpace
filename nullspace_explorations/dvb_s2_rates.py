def encode_long(params: DVBS2Params, high_rows, low_rows, info_bits: np.ndarray) -> np.ndarray:
    n, k, M, q, nmk = params.n, params.k, params.M, params.q, params.n_minus_k
    info_bits = np.asarray(info_bits)
    assert info_bits.shape == (k,), f"info shape {info_bits.shape} != ({k},)"
    all_rows = list(high_rows) + list(low_rows)
    parity = np.zeros(nmk, dtype=np.int8)
    for m in range(k):
        if int(info_bits[m]) == 0:
            continue
        for x in all_rows[m // M]:
            parity[(x + (m % M) * q) % nmk] ^= 1
    for j in range(1, nmk):
        parity[j] ^= parity[j - 1]
    return np.concatenate([info_bits.astype(np.int8), parity])

def make_code(rate: str) -> LDPCCode:
    if rate not in LONG_RATE_TABLES:
        raise KeyError(f"unknown rate {rate!r}; have {ALL_RATES}")
    high, low, p = LONG_RATE_TABLES[rate]
    if rate == "1/2":
        return LDPCCode("dvbs2-long-r1/2", build_dvbs2_h_long_rate12(), p.n, p.k, encode_dvbs2_long_rate12)
    H = _build_h_from_tables(high_rows=high, low_rows=low, params=p)
    enc = lambda info, _h=high, _l=low, _p=p: encode_long(_p, _h, _l, info)
    return LDPCCode(f"dvbs2-long-r{rate}", H, p.n, p.k, enc)

def verify(rate: str, seed: int = 0) -> dict:
    high, low, p = LONG_RATE_TABLES[rate]
    n, k, nmk = p.n, p.k, p.n_minus_k
    code = make_code(rate)
    H = code.H
    pred = {}
    for r in list(high) + list(low):
        d = len(r); pred[d] = pred.get(d, 0) + 1
    pred_info = {d: c * p.M for d, c in pred.items()}
    pred_col = dict(pred_info)
    pred_col[1] = pred_col.get(1, 0) + 1
    pred_col[2] = pred_col.get(2, 0) + (nmk - 1)
    pred_nnz = sum(d * c * p.M for d, c in pred.items()) + (2 * nmk - 1)
    cw = np.asarray(H.sum(axis=0)).ravel()
    col_dist = {int(w): int((cw == w).sum()) for w in np.unique(cw)}
    rw = np.asarray(H.sum(axis=1)).ravel()
    rng = np.random.default_rng(seed)
    info = rng.integers(0, 2, size=k, dtype=np.int8)
    c = code.encode(info)
    synd = int(code.syndrome(c).sum())
    sys_ok = bool(np.array_equal(code.info_bit_extract(c), info))
    res = {
        "rate": rate, "n": n, "k": k, "q": p.q, "rows": len(high) + len(low),
        "shape_ok": H.shape == (nmk, n),
        "nnz": int(H.nnz), "links_total": LINKS_TOTAL[rate],
        "nnz_ok": int(H.nnz) == pred_nnz == LINKS_TOTAL[rate],
        "col_dist_ok": col_dist == pred_col,
        "col_dist": col_dist,
        "rowwt_min": int(rw.min()), "rowwt_max": int(rw.max()),
        "Hc0_ok": synd == 0, "systematic_ok": sys_ok,
    }
    if rate == "1/2":
        Hf = build_dvbs2_h_long_rate12()
        res["frozen_H_identical"] = bool((H != Hf).nnz == 0)
    res["all_ok"] = all(res[k2] for k2 in ["shape_ok", "nnz_ok", "col_dist_ok", "Hc0_ok", "systematic_ok"] + (["frozen_H_identical"] if rate == "1/2" else []))
    return res